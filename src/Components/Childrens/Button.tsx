import React from 'react';

type ButtonProps = {
    text: string;
    icon?: any;
    className?: string;
};

const Button = ({ text, icon, className = "" }: ButtonProps) => {
    return (
        <button className={`${className} flex gap-3 items-center px-6 py-3 bg-blue-600 text-white rounded-lg hover:bg-blue-700 transition-colors`}>
            {icon && <span className="">{icon}</span>}
            {text}
        </button>
    );
};

export default Button;
